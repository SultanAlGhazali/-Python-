import math

# Лабораторна робота №1. Завдання 1 (Варіант 1)
# Обчислення виразу: z = cos(a) + sin(a) + cos(3a) + sin(3a)

# Запитуємо введення кута альфа від користувача (у градусах)
alpha_deg = float(input("Введіть значення кута alpha (в градусах): "))

# Переводимо градуси в радіани, оскільки функції math працюють з радіанами
alpha = math.radians(alpha_deg)

# Обчислюємо значення виразу за формулою з методички
z = math.cos(alpha) + math.sin(alpha) + math.cos(3 * alpha) + math.sin(3 * alpha)

# Виводимо результат
print(f"Результат обчислення z = {z:.4f}")
