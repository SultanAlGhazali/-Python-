# Лабораторна робота №1. Завдання 3 (Варіант 1)
# Матриця 5х4. Поміняти місцями перший і третій рядок.

# Створюємо початкову матрицю 5х4 (5 рядків, 4 стовпці)
matrix = [
    [10, 11, 12, 13],  # Перший рядок (індекс 0)
    [20, 21, 22, 23],  # Другий рядок (індекс 1)
    [30, 31, 32, 33],  # Третій рядок (індекс 2)
    [40, 41, 42, 43],  # Четвертий рядок (індекс 3)
    [50, 51, 52, 53]   # П'ятий рядок (індекс 4)
]

print("Початкова матриця:")
for row in matrix:
    print(row)

# Міняємо місцями перший (індекс 0) та третій (індекс 2) рядки
matrix[0], matrix[2] = matrix[2], matrix[0]

print("\nМатриця після зміни 1-го та 3-го рядків:")
for row in matrix:
    print(row)
