# Виведення n-го елемента послідовності Фібоначчі

n = int(input("Введіть номер елемента Фібоначчі (n >= 0): "))

# Базові значення згідно з методичкою (F0=1, F1=1, F2=1)
if n == 0 or n == 1 or n == 2:
    fn = 1
else:
    f0, f1, f2 = 1, 1, 1
    # Обчислюємо кожен наступний елемент як суму двох попередніх
    for _ in range(3, n + 1):
        fn = f1 + f2
        f1 = f2
        f2 = fn

print(f"{n}-й елемент послідовності Фібоначчі: {fn}")
