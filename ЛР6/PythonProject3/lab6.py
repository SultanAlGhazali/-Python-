# 1. Створюємо базу даних студентів у вигляді словника
# Ключ — прізвище, значення — ще один словник з курсом та списком оцінок
students_db = {
    "Аль-Газалі": {"курс": 3, "оцінки": [95, 90, 88, 100, 92]},
    "Іванов": {"курс": 1, "оцінки": [75, 80, 68, 70]},
    "Петров": {"курс": 2, "оцінки": [85, 82, 90, 88, 84]}
}


def get_average_score(surname):
    """Функція для обчислення середнього балу студента."""
    if surname in students_db:
        grades = students_db[surname]["оцінки"]
        # Рахуємо суму оцінок та ділимо на їх кількість
        return sum(grades) / len(grades)
    return None


def main():
    print("--- Робота зі словниками (База даних студентів) ---\n")

    # 2. Виводимо інформацію про всіх студентів з бази
    print("Список усіх студентів у базі:")
    for surname, info in students_db.items():
        print(f"Студент: {surname:<12} | Курс: {info['курс']} | Оцінки: {info['оцінки']}")

    # 3. Демонстрація пошуку конкретного студента за прізвищем
    search_name = "Аль-Газалі"
    print(f"\nПошук студента за прізвищем '{search_name}':")

    avg = get_average_score(search_name)
    if avg is not None:
        print(f"Студента знайдено!")
        print(f"- Курс: {students_db[search_name]['курс']}")
        print(f"- Середній бал студента {search_name}: {avg:.2f}")
    else:
        print("Студента з таким прізвищем не знайдено.")


if __name__ == "__main__":
    main()
