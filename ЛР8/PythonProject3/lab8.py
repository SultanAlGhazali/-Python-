import datetime


def bot_interface():
    print("=== Вітаємо у фінальному скрипті-помічнику! ===")
    print("Султан, доступні команди: 'привіт', 'дата', 'рахуй [число1] [число2]', 'вихід'\n")

    while True:
        # Зчитуємо команду користувача, прибираємо зайві пробіли та переводимо у нижній регістр
        user_input = input("Султан ➔ ").strip().lower()

        # Обробка команди "привіт"
        if user_input == "привіт":
            print("Бот ➔ Привіт, Султан! Всі лабораторні майже готові. Чим можу допомогти?")

        # Обробка команди "дата"
        elif user_input == "дата":
            now = datetime.datetime.now().strftime("%d.%m.%Y %H:%M")
            print(f"Бот ➔ Поточна дата та час: {now}")

        # Обробка команди "рахуй" з парсингом чисел
        elif user_input.startswith("рахуй"):
            try:
                # Розбиваємо рядок за пробілами ("рахуй 15 25" -> ["рахуй", "15", "25"])
                parts = user_input.split()
                num1 = float(parts[1])
                num2 = float(parts[2])
                print(f"Бот ➔ Результат додавання чисел: {num1 + num2}")
            except (IndexError, ValueError):
                print("Бот ➔ Помилка! Правильний формат команди: рахуй 5 10")

        # Обробка команди "вихід"
        elif user_input == "вихід":
            print("Бот ➔ До побачення, Султан! Весь комплекс з 8 лабораторних робіт виконано успішно!")
            break

        # Якщо введено невідому команду
        else:
            print("Бот ➔ Невідома команда. Спробуйте: 'привіт', 'дата', 'рахуй 5 10' або 'вихід'")


if __name__ == "__main__":
    bot_interface()
